# Math
# min() = เป็นการถามว่าจำนวนไหนมีค่าต่ำสุด
# max() = เป็นการถามว่าจำนวนไหนมีค่าสูงสุด
# abs() = absolute => ค่าสัมบูรณ์
# pow() = เป็นการถามค่าเลขยกกำลัง(ต้องใส่พารามิเตอร์ 2 พารามิเตอร์ในการหาค่านี้)

x=min(3,4,5,-10,30,40)
print(x)

y=max(3,4,5,-10,30,40)
print(y)

z=abs(-50)
print(z)

p=pow(5,10) # 5**10 = ?
print(p)